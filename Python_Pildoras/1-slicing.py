#extraer cadenas mas grandes del caracter principal
cadena = "hola mundooo"
#         01234567891011
print(cadena[0:5])  # extrae hola
print(cadena[5:10])  # extrae mundo
print(cadena[:10])  # extrae hola mundo
print(cadena[0:])  # extrae hola mundoo
print(cadena[:])  # extrae hola mundoo
print(cadena[::2])  # extrae de inicio a fin haciendo saltos de dos en dos
print(cadena[-7:-1])  # del menos 7 al menos uno  
print(cadena[::-1])  # invertir el orden de los caracteres saltando de uno en uno

